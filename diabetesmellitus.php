                               <p>Diabetes mellitus is a metabolic condition. It can’t be treated solely by lowering blood sugar levels. In contrast to modern medicine, Ayurveda approaches diabetes in a unique way. It literally revitalizes the body, not only to ‘control’ sugar levels but also to prevent further complications.</p>

                              <p>Ayurveda has a unique approach to diabetes treatment which includes diet, medicines and medications, exercise and panchakarma.</p>
                                 
                              

                             <p>During digestion, food is broken down into its basic components. Carbohydrates are broken down into simple sugars, primarily glucose. Glucose is a critically important source of energy for the body's cells. To provide energy to the cells, glucose needs to leave the bloodstream and get inside the cells.
                             </p>

                             <p>An organ in the abdomen called the pancreas produces a hormone called insulin, which is essential to helping glucose get into the body's cells. In a person without diabetes, the pancreas produces more insulin whenever blood levels of glucose rise (for example, after a meal), and the insulin signals the body's cells to take in the glucose. In diabetes, either the pancreas's ability to produce insulin or the cells' response to insulin is altered.
                             </p>

<h3>Symptoms
</h3>

<p>Diabetes initially might not cause any symptoms. It can sometimes be caught early with a routine blood test before a person develops symptoms.
</p>

<h5>When diabetes does cause symptoms, they may include
</h5>

<ul>
    <li>excessive urination
    </li>
    <li>excessive thirst, leading to drinking a lot of fluid
    </li>
    <li>weight loss.
    </li>
    <li>People with diabetes also have an increased susceptibility to infections, especially yeast (Candida) infections.
    </li>
    
</ul>

<p>When the amount of insulin in the blood stream is too low, extremely high blood sugar levels can lead to dangerous complications. The body can become too acidic, a condition called diabetic ketoacidosis. Or the blood sugar level gets so high, the person becomes severely dehydrated. 
</p>

<h3> Symptoms of hypoglycemia include
</h3>

<li>sweating</li>
<li>trembling</li>
<li>dizziness</li>
<li>hunger</li>
<li>confusion</li>
<p>seizures and loss of consciousness (if hypoglycemia is not recognized and corrected).
You can correct hypoglycemia by eating or drinking something that has carbohydrates. This raises your blood sugar level.
</p>

<h3>Retinopathy- Nerve damage of optic nerve 
</h3>

<p>Neuropathy — This is another term for nerve damage. The most common type is peripheral neuropathy, which affects nerves in the feet and hands. The nerves to the legs are damaged first, causing pain and numbness in the feet. This can advance to cause symptoms in the legs and hands. Damage to the nerves that control digestion, sexual function, and urination can also occur.</p>

<p>Foot problems — Any sores, injuries, or blisters on the feet can lead to the following complications:
</p>
<p>If peripheral neuropathy causes numbness, a person may not feel any irritation or injury that occurs on the foot. The skin can break down and form an ulcer, and the ulcer can get infected.
</p>
<h3>Blood circulation can be poor, leading to slow healing of any foot injuries
</h3>
<p>Nephropathy — This refers to damage to the kidneys. This complication is more likely if blood sugars remain elevated and high blood pressure is not treated aggressively.
</p>

<p>Diagnosis
Diabetes is diagnosed through blood tests that detect the level of glucose in the blood.
</p>

<p>Keep control of your blood sugar. This helps reduce the risk of most complications.</p>
