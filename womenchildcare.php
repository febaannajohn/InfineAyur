So at a minimum, it is recommend for a new mama to spend atleast her first two weeks postpartum in a state of complete rest.
</p>

<p>Due to the high vata in your body, your digestive fire, or agni, is compromised. Since digestion itself takes a considerable amount of energy, during this time, it’s especially important to fuel your body with foods that are easy to digest, so your body has the maximum energy for performing it’s primary functions – including postpartum recovery and lactation

</p>
<p>Between breastfeeding, taking care of a newborn and the lack of sleep, many new moms forget to nourish themselves at regular intervals and find their energy levels or milk supply drop. So besides eating the right foods, it’s important to eat small meals at regular intervals, too – and keep your vata in check.

Sometimes it works well to time your food intake with your baby’s feeds. For example, if you’re breastfeeding, eat something before/during/after every feed. This way, you’ll always be replenishing your body to provide energy for the next feed.
