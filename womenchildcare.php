<h3>Mother and baby care
</h3>
<p>In the science, Ayurveda, the days after birth are a sacred period. its a time of rest, self-care, and love.

Each person naturally leans toward one dosha and this affects their body type, temperament and health.
</p>

<p>When you give birth, your vata increases because of the now empty abdomen, loss of fluids and blood, and decreased energy, creating an imbalance in the body often leading to postpartum anxiety.

Ayurveda care is to counter vata, heal from childbirth and set yourself on a path of health and balance in the future.
</p>
<p>
Ayurvedic texts suggest that the first forty-two days after giving birth should be focused on rest, rejuvination and bonding with your newborn, after the incredible changes your body, mind and spirit have just gone through.
The focus of this healing period is to bring the vata dosha into balance, restore the reproductive tissues, and promote healthy lactation channels. This ensures the mother’s health is cared for and in turn, allows for better bonding between mother and baby.
</p>
<p> Co-sleeping is a great way for new moms to rest when their baby is sleeping without feeling as anxious.

